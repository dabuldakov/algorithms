package org.example.search;

public class Parent {
    private long sum;
    private int counter;
    private int[][] array;

    public long getSum() {
        return sum;
    }

    public void setSum(long sum) {
        this.sum = sum;
    }

    public int getCounter() {
        return counter;
    }

    public void setCounter(int counter) {
        this.counter = counter;
    }

    public int[][] getArrays() {
        return array;
    }

    public void setArrays(int[][] array) {
        this.array = array;
    }
}
